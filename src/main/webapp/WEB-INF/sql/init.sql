-- ==========================================
-- 1. 시퀀스 생성
-- ==========================================
-- 예약 번호 생성용 시퀀스
CREATE SEQUENCE SEQ_RESERVATION START WITH 1 INCREMENT BY 1;

-- 비품 대여 번호 생성용 시퀀스
CREATE SEQUENCE SEQ_RENTAL START WITH 1 INCREMENT BY 1;

-- ==========================================
-- 2. 테이블 생성 (DTO 기준 최신화 완료)
-- ==========================================
-- 1. 사원 테이블 (EmployeeDTO 기준)
CREATE TABLE EMPLOYEE (
    EMP_NO NUMBER PRIMARY KEY,
    EMP_PW VARCHAR2(50),
    EMP_NAME VARCHAR2(50) NOT NULL,
    EMP_LEVEL NUMBER NOT NULL,
    MANAGER VARCHAR2(1) DEFAULT 'N'
);

-- 2. 회의실 테이블 (RoomDTO 기준)
CREATE TABLE ROOM (
    ROOM_ID VARCHAR2(20) PRIMARY KEY,
    ROOM_NAME VARCHAR2(100) NOT NULL,
    CAPACITY NUMBER DEFAULT 0,
    HAS_BEAM VARCHAR2(1) DEFAULT 'N',
    DESCRIPTION VARCHAR2(500)
);

-- 3. 비품 마스터 테이블 (EquipmentDTO 기준)
CREATE TABLE EQUIPMENT (
    EQ_NO NUMBER PRIMARY KEY,
    EQ_NAME VARCHAR2(100) NOT NULL,
    TOTAL_COUNT NUMBER DEFAULT 0,
    REMAIN_COUNT NUMBER DEFAULT 0
);

-- 4. 예약 테이블 (ReservationDTO 최신 반영: EXPIRE_TIME 추가)
CREATE TABLE RESERVATION (
    RES_NO NUMBER PRIMARY KEY,
    EMP_NO NUMBER REFERENCES EMPLOYEE(EMP_NO),
    ROOM_ID VARCHAR2(20) REFERENCES ROOM(ROOM_ID),
    RES_DATE DATE NOT NULL,
    START_TIME VARCHAR2(10) NOT NULL,
    END_TIME VARCHAR2(10) NOT NULL,
    PURPOSE VARCHAR2(200),
    STATUS VARCHAR2(20) DEFAULT '예약완료',
    EXPIRE_TIME TIMESTAMP -- 5분 임시 선점 타이머용 컬럼
);

-- 5. 비품 대여 이력 테이블 (RentalHistoryDTO 최신 반영: REQ_COUNT 추가)
CREATE TABLE RENTAL_HISTORY (
    RENTAL_NO NUMBER PRIMARY KEY,
    TITLE VARCHAR2(100),
    EMP_NO NUMBER REFERENCES EMPLOYEE(EMP_NO),
    EQ_NO NUMBER REFERENCES EQUIPMENT(EQ_NO),
    REQ_COUNT NUMBER DEFAULT 1, -- 대여 신청 수량 추가
    RENTAL_DATE DATE NOT NULL,
    RETURN_DATE DATE NOT NULL,
    STATUS VARCHAR2(20) DEFAULT '승인대기',
    APPROVAL_STEP NUMBER DEFAULT 1,
    SIGN1 VARCHAR2(50),
    SIGN1_DATE DATE,
    SIGN2 VARCHAR2(50),
    SIGN2_DATE DATE,
    SIGN3 VARCHAR2(50),
    SIGN3_DATE DATE,
    SIGN4 VARCHAR2(50),
    SIGN4_DATE DATE,
    SIGN5 VARCHAR2(50),
    SIGN5_DATE DATE
);

